async function debugActionOrder() {
  try {
    const wasmModule = await import('../wasm-wrapper/pkg/wasm_wrapper.js')
    const { default: init, GameWrapper } = wasmModule

    const wasmPath = path.join(
      process.cwd(),
      '../wasm-wrapper/pkg/wasm_wrapper_bg.wasm'
    )
    const wasmBuffer = fs.readFileSync(wasmPath)
    await init(wasmBuffer)

    console.log('=== Debugging Action Execution Order ===')
    console.log()

    const gameWrapper = new GameWrapper(JSON.stringify(testConfig))
    gameWrapper.new_game()

    console.log('Character starts at pos=220, very close to wall at x=224')
    console.log('Behaviors:')
    console.log('  [0, 0] - IS_WALL_LEANING -> TURN_AROUND')
    console.log('  [1, 1] - ALWAYS -> RUN')
    console.log()

    for (let frame = 0; frame < 10; frame++) {
      const state = JSON.parse(gameWrapper.get_characters_json())
      const char = state[0]
      const posX = char.position[0][0] / char.position[0][1]
      const velX = char.velocity[0][0] / char.velocity[0][1]
      const dirValue = char.dir[0]
      const collision = char.collision

      console.log(
        `Frame ${frame}: pos=${posX.toFixed(1)}, vel=${velX.toFixed(
          1
        )}, dir=${dirValue}, collision=[${collision.join(', ')}]`
      )

      // Analyze what should happen:
      if (collision[1] || collision[3]) {
        // Right or left wall collision
        console.log(
          `  → Wall collision detected! Both conditions should trigger:`
        )
        console.log(`    - IS_WALL_LEANING: true → TURN_AROUND action`)
        console.log(`    - ALWAYS: true → RUN action`)
        console.log(
          `  → Question: Which action executes first? Do they conflict?`
        )
      } else {
        console.log(`  → No wall collision. Only ALWAYS condition triggers:`)
        console.log(`    - ALWAYS: true → RUN action`)
      }

      gameWrapper.step_frame()
    }

    console.log()
    console.log('=== HYPOTHESIS ===')
    console.log('The problem might be:')
    console.log('1. Both TURN_AROUND and RUN actions execute in the same frame')
    console.log('2. TURN_AROUND changes direction mid-frame')
    console.log(
      '3. RUN reads the NEW direction but something resets velocity to 0'
    )
    console.log('4. OR: Actions execute in wrong order')
    console.log(
      '5. OR: There is a cooldown/energy issue preventing RUN from executing'
    )

    gameWrapper.free()
  } catch (error) {
    console.error('Error:', error)
  }
}

debugActionOrder()
