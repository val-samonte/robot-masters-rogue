async function testRunOnly() {
  try {
    const wasmModule = await import('../wasm-wrapper/pkg/wasm_wrapper.js')
    const { default: init, GameWrapper } = wasmModule

    const wasmPath = path.join(
      process.cwd(),
      '../wasm-wrapper/pkg/wasm_wrapper_bg.wasm'
    )
    const wasmBuffer = fs.readFileSync(wasmPath)
    await init(wasmBuffer)

    console.log('=== Testing RUN Action Only ===')

    const gameWrapper = new GameWrapper(JSON.stringify(testConfig))
    gameWrapper.new_game()

    console.log('Initial state:')
    const initial = JSON.parse(gameWrapper.get_characters_json())
    const char = initial[0]
    console.log(`Direction: ${char.dir[0]} (should be 2 for right)`)
    console.log(
      `Move speed: ${char.move_speed[0]}/${char.move_speed[1]} = ${
        char.move_speed[0] / char.move_speed[1]
      }`
    )
    console.log(
      `Expected calculation: dir=2 → script_dir=1.0, speed=2.0 → velocity=2.0`
    )

    for (let frame = 0; frame < 10; frame++) {
      const state = JSON.parse(gameWrapper.get_characters_json())
      const char = state[0]
      const posX = char.position[0][0] / char.position[0][1]
      const velX = char.velocity[0][0] / char.velocity[0][1]
      console.log(
        `Frame ${frame}: pos=${posX.toFixed(1)}, vel=${velX.toFixed(1)}, dir=${
          char.dir[0]
        }`
      )

      if (Math.abs(velX) > 0.1) {
        console.log('✅ RUN action working - velocity set correctly')
        console.log(`Expected: +2.0, Actual: ${velX.toFixed(1)}`)
        break
      }

      gameWrapper.step_frame()
    }

    gameWrapper.free()
  } catch (error) {
    console.error('Error:', error)
  }
}

testRunOnly()
